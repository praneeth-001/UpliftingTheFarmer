package com.voidmain.pojo;

public class Machinary {

	private int machanaryId;
	private String name;
	private String owner;
	private float leaseAmountPerHour;
	private String isDriverSuppiled;
	private String address;
	private String mobileNumber;
	public int getMachanaryId() {
		return machanaryId;
	}
	public void setMachanaryId(int machanaryId) {
		this.machanaryId = machanaryId;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getOwner() {
		return owner;
	}
	public void setOwner(String owner) {
		this.owner = owner;
	}
	public float getLeaseAmountPerHour() {
		return leaseAmountPerHour;
	}
	public void setLeaseAmountPerHour(float leaseAmountPerHour) {
		this.leaseAmountPerHour = leaseAmountPerHour;
	}
	public String getIsDriverSuppiled() {
		return isDriverSuppiled;
	}
	public void setIsDriverSuppiled(String isDriverSuppiled) {
		this.isDriverSuppiled = isDriverSuppiled;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getMobileNumber() {
		return mobileNumber;
	}
	public void setMobileNumber(String mobileNumber) {
		this.mobileNumber = mobileNumber;
	}
}
